#include "libft.h"

char		*ft_strncpy(char *dest, const char *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	while (n)
	{
		if (*s)
			*d++ = *s++;
		else
			*d++ = '\0';
		n--;
	}
	return (dest);
}
